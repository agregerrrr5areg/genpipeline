/*
 * GPU Marching Cubes Kernel
 *
 * Extracts an isosurface mesh from a voxel grid entirely on the GPU.
 * Each CUDA thread processes one voxel cube, looks up its configuration
 * in the standard MC tables, and atomically writes its triangles to
 * the output buffer.
 *
 * Output: flat triangle soup — every 3 consecutive vertices form one triangle.
 * No vertex deduplication (fast; use trimesh.merge_vertices() if needed).
 *
 * Cube corner / edge numbering (Lorensen & Cline 1987):
 *
 *       7 ---- 6
 *      /|     /|
 *     4 ---- 5 |
 *     | 3 ---| 2
 *     |/     |/
 *     0 ---- 1
 *
 * Corners: 0=(x,y,z) 1=(x+1,y,z) 2=(x+1,y+1,z) 3=(x,y+1,z)
 *          4=(x,y,z+1) 5=(x+1,y,z+1) 6=(x+1,y+1,z+1) 7=(x,y+1,z+1)
 * Edges: 0-3 on bottom face, 4-7 on top face, 8-11 vertical pillars
 */

