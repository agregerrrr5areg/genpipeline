// ── Helper: linear index into voxel grid ──────────────────────────────────────

__device__ __forceinline__ float vox(
    const float* g, int x, int y, int z, int Y, int Z)
{
    return g[x * Y * Z + y * Z + z];
}

// ── Helper: interpolate vertex on edge between p0 and p1 ─────────────────────

__device__ __forceinline__ void interp(
    float iso,
    float x0, float y0, float z0, float v0,
    float x1, float y1, float z1, float v1,
    float* ox, float* oy, float* oz)
{
    float t = (fabsf(v1 - v0) < 1e-6f) ? 0.5f : (iso - v0) / (v1 - v0);
    *ox = x0 + t * (x1 - x0);
    *oy = y0 + t * (y1 - y0);
    *oz = z0 + t * (z1 - z0);
}


// ── Main kernel ───────────────────────────────────────────────────────────────

__global__ void mc_kernel(
    const float* __restrict__ voxels,      // [X, Y, Z] float32
    float*       __restrict__ out_verts,   // [max_tris*3, 3] float32 (output)
    int*         __restrict__ tri_count,   // scalar atomic counter
    int X, int Y, int Z,
    float ox_world, float oy_world, float oz_world,
    float voxel_size,
    float isovalue,
    int max_tris)
{
    int idx = blockIdx.x * blockDim.x + threadIdx.x;
    int total_cubes = (X-1) * (Y-1) * (Z-1);
    if (idx >= total_cubes) return;

    // Cube coordinates
    int xi = idx / ((Y-1) * (Z-1));
    int yi = (idx / (Z-1)) % (Y-1);
    int zi = idx % (Z-1);

    // 8 corner values
    float cv[8];
    cv[0] = vox(voxels, xi,   yi,   zi,   Y, Z);
    cv[1] = vox(voxels, xi+1, yi,   zi,   Y, Z);
    cv[2] = vox(voxels, xi+1, yi+1, zi,   Y, Z);
    cv[3] = vox(voxels, xi,   yi+1, zi,   Y, Z);
    cv[4] = vox(voxels, xi,   yi,   zi+1, Y, Z);
    cv[5] = vox(voxels, xi+1, yi,   zi+1, Y, Z);
    cv[6] = vox(voxels, xi+1, yi+1, zi+1, Y, Z);
    cv[7] = vox(voxels, xi,   yi+1, zi+1, Y, Z);

    // Build cube index
    int cube_idx = 0;
    if (cv[0] >= isovalue) cube_idx |= 1;
    if (cv[1] >= isovalue) cube_idx |= 2;
    if (cv[2] >= isovalue) cube_idx |= 4;
    if (cv[3] >= isovalue) cube_idx |= 8;
    if (cv[4] >= isovalue) cube_idx |= 16;
    if (cv[5] >= isovalue) cube_idx |= 32;
    if (cv[6] >= isovalue) cube_idx |= 64;
    if (cv[7] >= isovalue) cube_idx |= 128;

    if (d_edge_table[cube_idx] == 0) return;  // fully inside or outside

    // 8 corner world positions
    float wx[8], wy[8], wz[8];
    wx[0] = ox_world + xi * voxel_size;
    wy[0] = oy_world + yi * voxel_size;
    wz[0] = oz_world + zi * voxel_size;

    wx[1] = wx[0] + voxel_size; wy[1] = wy[0];              wz[1] = wz[0];
    wx[2] = wx[0] + voxel_size; wy[2] = wy[0] + voxel_size; wz[2] = wz[0];
    wx[3] = wx[0];              wy[3] = wy[0] + voxel_size; wz[3] = wz[0];
    wx[4] = wx[0];              wy[4] = wy[0];              wz[4] = wz[0] + voxel_size;
    wx[5] = wx[0] + voxel_size; wy[5] = wy[0];              wz[5] = wz[0] + voxel_size;
    wx[6] = wx[0] + voxel_size; wy[6] = wy[0] + voxel_size; wz[6] = wz[0] + voxel_size;
    wx[7] = wx[0];              wy[7] = wy[0] + voxel_size; wz[7] = wz[0] + voxel_size;

    // Interpolate all 12 edge vertices
    float ex[12], ey[12], ez[12];
    int et = d_edge_table[cube_idx];

    if (et & 0x001) interp(isovalue, wx[0],wy[0],wz[0],cv[0], wx[1],wy[1],wz[1],cv[1], &ex[0], &ey[0], &ez[0]);
    if (et & 0x002) interp(isovalue, wx[1],wy[1],wz[1],cv[1], wx[2],wy[2],wz[2],cv[2], &ex[1], &ey[1], &ez[1]);
    if (et & 0x004) interp(isovalue, wx[2],wy[2],wz[2],cv[2], wx[3],wy[3],wz[3],cv[3], &ex[2], &ey[2], &ez[2]);
    if (et & 0x008) interp(isovalue, wx[3],wy[3],wz[3],cv[3], wx[0],wy[0],wz[0],cv[0], &ex[3], &ey[3], &ez[3]);
    if (et & 0x010) interp(isovalue, wx[4],wy[4],wz[4],cv[4], wx[5],wy[5],wz[5],cv[5], &ex[4], &ey[4], &ez[4]);
    if (et & 0x020) interp(isovalue, wx[5],wy[5],wz[5],cv[5], wx[6],wy[6],wz[6],cv[6], &ex[5], &ey[5], &ez[5]);
    if (et & 0x040) interp(isovalue, wx[6],wy[6],wz[6],cv[6], wx[7],wy[7],wz[7],cv[7], &ex[6], &ey[6], &ez[6]);
    if (et & 0x080) interp(isovalue, wx[7],wy[7],wz[7],cv[7], wx[4],wy[4],wz[4],cv[4], &ex[7], &ey[7], &ez[7]);
    if (et & 0x100) interp(isovalue, wx[0],wy[0],wz[0],cv[0], wx[4],wy[4],wz[4],cv[4], &ex[8], &ey[8], &ez[8]);
    if (et & 0x200) interp(isovalue, wx[1],wy[1],wz[1],cv[1], wx[5],wy[5],wz[5],cv[5], &ex[9], &ey[9], &ez[9]);
    if (et & 0x400) interp(isovalue, wx[2],wy[2],wz[2],cv[2], wx[6],wy[6],wz[6],cv[6], &ex[10],&ey[10],&ez[10]);
    if (et & 0x800) interp(isovalue, wx[3],wy[3],wz[3],cv[3], wx[7],wy[7],wz[7],cv[7], &ex[11],&ey[11],&ez[11]);

    // Count triangles this cube emits
    int n_tris = 0;
    for (int i = 0; d_tri_table[cube_idx][i] != -1; i += 3) ++n_tris;
    if (n_tris == 0) return;

    // Atomically claim output slots
    int slot = atomicAdd(tri_count, n_tris);
    if (slot + n_tris > max_tris) return;  // overflow guard

    // Write triangles (each triangle = 3 vertices × 3 floats)
    for (int i = 0; d_tri_table[cube_idx][i] != -1; i += 3) {
        int e0 = d_tri_table[cube_idx][i];
        int e1 = d_tri_table[cube_idx][i+1];
        int e2 = d_tri_table[cube_idx][i+2];

        int base = (slot + i/3) * 9;
        out_verts[base+0] = ex[e0]; out_verts[base+1] = ey[e0]; out_verts[base+2] = ez[e0];
        out_verts[base+3] = ex[e1]; out_verts[base+4] = ey[e1]; out_verts[base+5] = ez[e1];
        out_verts[base+6] = ex[e2]; out_verts[base+7] = ey[e2]; out_verts[base+8] = ez[e2];
    }
}


// ── C++ entry point ───────────────────────────────────────────────────────────

std::vector<torch::Tensor> marching_cubes_cuda(
    torch::Tensor voxels,    // [X, Y, Z] float32 CUDA
    float isovalue,
    float ox, float oy, float oz,
    float voxel_size)
{
    TORCH_CHECK(voxels.is_cuda(),  "voxels must be a CUDA tensor");
    TORCH_CHECK(voxels.dim() == 3, "voxels must be 3-D");
    TORCH_CHECK(voxels.scalar_type() == torch::kFloat32, "voxels must be float32");

    int X = voxels.size(0), Y = voxels.size(1), Z = voxels.size(2);
    int total_cubes = (X-1) * (Y-1) * (Z-1);
    int max_tris    = total_cubes * 5;   // max 5 triangles per cube

    auto out_verts  = torch::zeros(
        {max_tris * 9},
        torch::TensorOptions().dtype(torch::kFloat32).device(voxels.device())
    );
    auto tri_count  = torch::zeros(
        {1},
        torch::TensorOptions().dtype(torch::kInt32).device(voxels.device())
    );

    int threads = 256;
    int blocks  = (total_cubes + threads - 1) / threads;

    mc_kernel<<<blocks, threads>>>(
        voxels.data_ptr<float>(),
        out_verts.data_ptr<float>(),
        tri_count.data_ptr<int>(),
        X, Y, Z,
        ox, oy, oz,
        voxel_size,
        isovalue,
        max_tris
    );

    cudaError_t err = cudaGetLastError();
    TORCH_CHECK(err == cudaSuccess,
        "mc_kernel launch failed: ", cudaGetErrorString(err));
    cudaDeviceSynchronize();

    int n_tris = tri_count.item<int>();
    // Reshape to [N*3, 3] — every 3 rows = one triangle
    auto verts = out_verts.slice(0, 0, n_tris * 9).reshape({n_tris * 3, 3});

    // Build trivial face array: [[0,1,2],[3,4,5],...]
    auto face_idx = torch::arange(n_tris * 3,
        torch::TensorOptions().dtype(torch::kInt32).device(voxels.device())
    ).reshape({n_tris, 3});

    return {verts, face_idx};
}


PYBIND11_MODULE(TORCH_EXTENSION_NAME, m) {
    m.def("marching_cubes_cuda", &marching_cubes_cuda,
          "GPU Marching Cubes isosurface extraction\n"
          "Args: voxels [X,Y,Z] float32, isovalue, ox, oy, oz, voxel_size\n"
          "Returns: [verts [N*3,3], faces [N,3]]");
}
